#include <bits//stdc++.h>
using namespace std;
int main()
{
    char ch;
    cin>>ch;
    if (ch=='u')
    {
        cout<<"Up"<<endl;
    }
    else if (ch == 'd')
    {
        cout<<"down"<<endl;
    }
    else if (ch == 'I')
    {
        cout<<"left"<<endl;
    }
    else if(ch == 'r')
    {
        cout<<"right"<<endl;
    }
    else
    {
        cout<<"no found the word"<<endl;
    }


    return 0;
}

